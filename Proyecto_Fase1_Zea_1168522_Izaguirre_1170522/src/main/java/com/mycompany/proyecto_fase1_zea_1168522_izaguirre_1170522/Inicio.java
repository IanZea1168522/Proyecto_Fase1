    private int buscarUsuario(String usuario, List<String> lista) 
    {
        for (int i = 0; i < lista.size(); i++) 
        {
            if (usuario.equals(lista.get(i).split("\\|")[0])) 
            {
                return i;
            }
        }
        return -1;
    }
    
    public List<String> Obtener(String strPath, String strError) 
    {
        File archivo = new File(strPath);
        if (archivo.exists()) 
        {
            try (BufferedReader leerArchivo = new BufferedReader(new FileReader(archivo))) 
            {
                List<String> listaPunteo = new ArrayList<>();
                String linea;
                while ((linea = leerArchivo.readLine()) != null) 
                {
                    if (!linea.isEmpty()) 
                    {
                        listaPunteo.add(linea);
                    }
                }
                return listaPunteo;
            }    
            catch (IOException ex) 
            {
                strError = ex.getMessage();
                return null;
            }
        } 
        else 
        {
            strError = "No existe el archivo";
            return null;
        }
    }
