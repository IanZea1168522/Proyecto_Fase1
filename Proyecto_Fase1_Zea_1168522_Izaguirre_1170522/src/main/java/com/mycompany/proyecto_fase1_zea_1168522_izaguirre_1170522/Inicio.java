    private void botonIngresoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonIngresoActionPerformed
        String usuario = textBoxUsuario1.getText();
        String contraseña = textBoxContraseña.getText();
        if(contraseña.isEmpty() || usuario.isEmpty()) 
        {
            JOptionPane.showMessageDialog(null, "Alguno de los campos está vacío, vuelva a intentarlo", "Error", WIDTH);
        }    
        else 
        {
            String strError = "";
            String rutaUs = "C:\\MEIA\\Usuario.txt";
            String rutaBit = "C:\\MEIA\\Bitacora.txt";
            List<String> listaUsu = Obtener(rutaUs, strError);
            List<String> listaBit = Obtener(rutaBit, strError);
            List<String> plantilla = new ArrayList<>();
            int indice = buscarUsuario(usuario, listaUsu);
            if (indice == -1) 
            {
                indice = buscarUsuario(usuario, listaBit);
                plantilla = listaBit;
            } 
            else 
            {
                plantilla = listaUsu;
            }
            if (indice != -1) 
            {
                if (contraseña.equals(plantilla.get(indice).split("\\|")[3])) 
                {
                    JOptionPane.showMessageDialog(null, "Puede proceder a su cuenta", "Listo", WIDTH);
                } 
                else 
                {
                    JOptionPane.showMessageDialog(null, "La contraseña es incorrecta, vuelva a intentarlo", "Error", WIDTH);
                }
            }   
            else 
            {
                JOptionPane.showMessageDialog(null, "Usuario no encontrado, vuelva a intentarlo o pruebe registrándose en el botón de abajo", "Error", WIDTH);
            }
        }
    }//GEN-LAST:event_botonIngresoActionPerformed

